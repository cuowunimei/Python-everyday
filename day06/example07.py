nums = [9, 1, 2, 3, 4, 5, 6, 7, 8]
# nums = [35, 12, 99, 58, 67, 42, 49, 31, 73]

# 整个做n - 1次，就可以把n - 1 个大数沉下去，最后一个就是最小的
for i in range(1, len(nums)):
    swapped = False
    # 做过的就不用再做了，已经是最大的了
    for j in range(0, len(nums) - i):
        if nums[j] > nums[j + 1]:
            nums[j], nums[j + 1] = nums[j + 1], nums[j]
            swapped = True
    # 如果一次都没交换，就终止循环
    if not swapped:
        break
print(nums)
