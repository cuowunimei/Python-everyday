[12, 35, 58, 67, 42, 99, 49, 31, 73]
[12, 35, 58, 67, 42, 49, 99, 31, 73]
[12, 35, 58, 67, 42, 49, 31, 99, 73]
[12, 35, 58, 67, 42, 49, 31, 73, 99]

[12, 35, 58, 67, 42, 49, 31, 73]
[12, 35, 58, 67, 42, 49, 31, 73]
[12, 35, 58, 42, 67, 49, 31, 73]
[12, 35, 58, 42, 49, 67, 31, 73]
